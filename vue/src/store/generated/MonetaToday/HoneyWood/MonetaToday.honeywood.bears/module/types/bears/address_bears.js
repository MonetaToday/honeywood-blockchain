import { util, configure, Writer, Reader } from "protobufjs/minimal";
export const protobufPackage = "MonetaToday.honeywood.bears";
const baseAddressBears = { address: "", bears: 0 };
export const AddressBears = {
    encode(message, writer = Writer.create()) {
        if (message.address !== "") {
            writer.uint32(10).string(message.address);
        }
        writer.uint32(18).fork();
        for (const v of message.bears) {
            writer.uint64(v);
        }
        writer.ldelim();
        return writer;
    },
    decode(input, length) {
        const reader = input instanceof Uint8Array ? new Reader(input) : input;
        let end = length === undefined ? reader.len : reader.pos + length;
        const message = { ...baseAddressBears };
        message.bears = [];
        while (reader.pos < end) {
            const tag = reader.uint32();
            switch (tag >>> 3) {
                case 1:
                    message.address = reader.string();
                    break;
                case 2:
                    if ((tag & 7) === 2) {
                        const end2 = reader.uint32() + reader.pos;
                        while (reader.pos < end2) {
                            message.bears.push(longToNumber(reader.uint64()));
                        }
                    }
                    else {
                        message.bears.push(longToNumber(reader.uint64()));
                    }
                    break;
                default:
                    reader.skipType(tag & 7);
                    break;
            }
        }
        return message;
    },
    fromJSON(object) {
        const message = { ...baseAddressBears };
        message.bears = [];
        if (object.address !== undefined && object.address !== null) {
            message.address = String(object.address);
        }
        else {
            message.address = "";
        }
        if (object.bears !== undefined && object.bears !== null) {
            for (const e of object.bears) {
                message.bears.push(Number(e));
            }
        }
        return message;
    },
    toJSON(message) {
        const obj = {};
        message.address !== undefined && (obj.address = message.address);
        if (message.bears) {
            obj.bears = message.bears.map((e) => e);
        }
        else {
            obj.bears = [];
        }
        return obj;
    },
    fromPartial(object) {
        const message = { ...baseAddressBears };
        message.bears = [];
        if (object.address !== undefined && object.address !== null) {
            message.address = object.address;
        }
        else {
            message.address = "";
        }
        if (object.bears !== undefined && object.bears !== null) {
            for (const e of object.bears) {
                message.bears.push(e);
            }
        }
        return message;
    },
};
